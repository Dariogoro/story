# Input variabel a untuk mengisi nilai
a = int(input("Inputkan nilai a: "))

# Tambahkan dengan 2
a += 2
print("Hasil penjumlahan a + 2 =", a)

# Kurangkan dengan 3
a -= 3
print("Hasil pengurangan a - 3 =", a)

# Kalikan dengan 4
a *= 4
print("Hasil perkalian a * 4 =", a)

# Bagi dengan 2
a /= 2
print("Hasil pembagian a / 2 =", a)
